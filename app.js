const fmtUsd = new Intl.NumberFormat("en-US", {
  style: "currency",
  currency: "USD",
  maximumFractionDigits: 4
});

const fmtInt = new Intl.NumberFormat("en-US");

function toPct(value) {
  return `${value.toFixed(2)}%`;
}

function signedPct(value) {
  const prefix = value >= 0 ? "+" : "";
  return `${prefix}${value.toFixed(2)}%`;
}

function setText(id, value, className) {
  const el = document.getElementById(id);
  el.textContent = value;
  if (className) {
    el.classList.remove("pos", "neg");
    el.classList.add(className);
  }
}

function parseDate(input) {
  return new Date(`${input}T00:00:00Z`);
}

function sum(values) {
  return values.reduce((acc, value) => acc + value, 0);
}

function postEngagement(post) {
  return post.likes + post.reposts + post.replies;
}

function getBeforeAfterCampaignData(campaign) {
  const postDate = parseDate(campaign.postDate);
  const beforeTimeline = campaign.timeline.filter((item) => parseDate(item.date) < postDate);
  const afterTimeline = campaign.timeline.filter((item) => parseDate(item.date) >= postDate);

  const beforePosts = campaign.posts.filter((post) => parseDate(post.date) < postDate);
  const afterPosts = campaign.posts.filter((post) => parseDate(post.date) >= postDate);

  return {
    beforeTimeline,
    afterTimeline,
    beforePosts,
    afterPosts
  };
}

function engagementRate(posts) {
  const totalImpressions = sum(posts.map((post) => post.impressions));
  if (!totalImpressions) {
    return 0;
  }

  const totalEngagement = sum(posts.map((post) => postEngagement(post)));
  return (totalEngagement / totalImpressions) * 100;
}

function topPosts(posts, count = 3) {
  return [...posts]
    .sort((a, b) => postEngagement(b) - postEngagement(a))
    .slice(0, count);
}

function calculateCampaignStats(campaign) {
  const data = getBeforeAfterCampaignData(campaign);

  const startFollowers = data.beforeTimeline.at(-1)?.followers ?? campaign.timeline[0].followers;
  const endFollowers = data.afterTimeline.at(-1)?.followers ?? campaign.timeline.at(-1).followers;
  const followerChange = endFollowers - startFollowers;
  const followerChangePct = (followerChange / startFollowers) * 100;

  const startPrice = data.beforeTimeline.at(-1)?.price ?? campaign.timeline[0].price;
  const endPrice = data.afterTimeline.at(-1)?.price ?? campaign.timeline.at(-1).price;
  const tokenChangePct = ((endPrice - startPrice) / startPrice) * 100;

  const beforeEngagementRate = engagementRate(data.beforePosts);
  const afterEngagementRate = engagementRate(data.afterPosts);
  const engagementChangePct = afterEngagementRate - beforeEngagementRate;

  const avgEngagementBefore =
    data.beforePosts.length === 0
      ? 0
      : sum(data.beforePosts.map((post) => postEngagement(post))) / data.beforePosts.length;
  const avgEngagementAfter =
    data.afterPosts.length === 0
      ? 0
      : sum(data.afterPosts.map((post) => postEngagement(post))) / data.afterPosts.length;

  const tokenReturnValue = (endPrice - startPrice) * campaign.tokensAllocated;
  const roiPct = ((tokenReturnValue - campaign.costUsd) / campaign.costUsd) * 100;

  return {
    ...data,
    startFollowers,
    endFollowers,
    followerChange,
    followerChangePct,
    startPrice,
    endPrice,
    tokenChangePct,
    beforeEngagementRate,
    afterEngagementRate,
    engagementChangePct,
    avgEngagementBefore,
    avgEngagementAfter,
    tokenReturnValue,
    roiPct
  };
}

function asPosNegClass(value) {
  return value >= 0 ? "pos" : "neg";
}

function renderKpis(project, campaign, stats) {
  setText(
    "tokenPriceValue",
    `${fmtUsd.format(stats.endPrice)} ${project.tokenSymbol}`,
    asPosNegClass(stats.tokenChangePct)
  );
  setText("tokenPriceChange", `${signedPct(stats.tokenChangePct)} vs pre-KOL`, asPosNegClass(stats.tokenChangePct));

  setText("followersValue", fmtInt.format(stats.endFollowers), asPosNegClass(stats.followerChangePct));
  setText(
    "followersChange",
    `${signedPct(stats.followerChangePct)} (${stats.followerChange >= 0 ? "+" : ""}${fmtInt.format(
      stats.followerChange
    )})`,
    asPosNegClass(stats.followerChangePct)
  );

  setText("engagementValue", toPct(stats.afterEngagementRate), asPosNegClass(stats.engagementChangePct));
  setText(
    "engagementChange",
    `${signedPct(stats.engagementChangePct)} points vs pre-KOL`,
    asPosNegClass(stats.engagementChangePct)
  );

  setText("roiValue", signedPct(stats.roiPct), asPosNegClass(stats.roiPct));
  setText(
    "roiMeta",
    `Spend ${fmtUsd.format(campaign.costUsd)} | Token return ${fmtUsd.format(stats.tokenReturnValue)}`,
    asPosNegClass(stats.roiPct)
  );

  document.getElementById("eventDateLabel").textContent = `KOL ${campaign.kolHandle} posted on ${campaign.postDate}`;
}

function renderComparisonChart(stats) {
  const mount = document.getElementById("comparisonChart");
  const metrics = [
    { label: "Followers", before: stats.startFollowers, after: stats.endFollowers },
    { label: "Engagement Rate %", before: stats.beforeEngagementRate, after: stats.afterEngagementRate },
    { label: "Avg Engagement/Post", before: stats.avgEngagementBefore, after: stats.avgEngagementAfter },
    { label: "Token Price ($)", before: stats.startPrice, after: stats.endPrice }
  ];

  const chartW = 940;
  const chartH = 320;
  const topPad = 16;
  const bottomPad = 68;
  const leftPad = 56;
  const rightPad = 16;
  const groupW = (chartW - leftPad - rightPad) / metrics.length;
  const barW = Math.min(44, groupW / 3.2);

  const maxValue = Math.max(
    ...metrics.flatMap((metric) => [metric.before, metric.after]),
    1
  );

  const yFor = (value) => {
    const chartHeight = chartH - topPad - bottomPad;
    return topPad + chartHeight - (value / maxValue) * chartHeight;
  };

  const yTicks = Array.from({ length: 5 }, (_, idx) => idx / 4);
  const tickLines = yTicks
    .map((ratio) => {
      const y = topPad + (chartH - topPad - bottomPad) * ratio;
      return `<line x1="${leftPad}" y1="${y}" x2="${chartW - rightPad}" y2="${y}" stroke="rgba(167,180,220,0.22)" />`;
    })
    .join("");

  const bars = metrics
    .map((metric, index) => {
      const groupX = leftPad + index * groupW + groupW / 2;
      const beforeY = yFor(metric.before);
      const afterY = yFor(metric.after);
      const baseY = chartH - bottomPad;

      return `
        <g>
          <rect x="${groupX - barW - 5}" y="${beforeY}" width="${barW}" height="${baseY - beforeY}" fill="#46d6ff" rx="4"></rect>
          <rect x="${groupX + 5}" y="${afterY}" width="${barW}" height="${baseY - afterY}" fill="#7c65ff" rx="4"></rect>
          <text x="${groupX}" y="${chartH - 42}" text-anchor="middle" fill="#a7b4dc" font-size="12">${metric.label}</text>
          <text x="${groupX - barW / 2 - 5}" y="${beforeY - 7}" text-anchor="middle" fill="#bcefff" font-size="11">${metric.before.toFixed(
            metric.before > 100 ? 0 : 2
          )}</text>
          <text x="${groupX + barW / 2 + 5}" y="${afterY - 7}" text-anchor="middle" fill="#d4cbff" font-size="11">${metric.after.toFixed(
            metric.after > 100 ? 0 : 2
          )}</text>
        </g>
      `;
    })
    .join("");

  mount.innerHTML = `
    <svg viewBox="0 0 ${chartW} ${chartH}" width="100%" height="320" role="img" aria-label="Before and after comparison chart">
      ${tickLines}
      ${bars}
      <line x1="${leftPad}" y1="${chartH - bottomPad}" x2="${chartW - rightPad}" y2="${chartH - bottomPad}" stroke="#7684b8" />
      <text x="${leftPad}" y="${chartH - 12}" fill="#a7b4dc" font-size="12">Legend:</text>
      <rect x="${leftPad + 58}" y="${chartH - 20}" width="13" height="13" fill="#46d6ff" rx="2"></rect>
      <text x="${leftPad + 77}" y="${chartH - 10}" fill="#a7b4dc" font-size="12">Before</text>
      <rect x="${leftPad + 140}" y="${chartH - 20}" width="13" height="13" fill="#7c65ff" rx="2"></rect>
      <text x="${leftPad + 159}" y="${chartH - 10}" fill="#a7b4dc" font-size="12">After</text>
    </svg>
  `;
}

function renderTimelineChart(campaign) {
  const mount = document.getElementById("timelineChart");
  const chartW = 940;
  const chartH = 320;
  const topPad = 20;
  const bottomPad = 54;
  const leftPad = 54;
  const rightPad = 20;

  const prices = campaign.timeline.map((point) => point.price);
  const followers = campaign.timeline.map((point) => point.followers);
  const priceMin = Math.min(...prices);
  const priceMax = Math.max(...prices);
  const followerMin = Math.min(...followers);
  const followerMax = Math.max(...followers);

  const chartInnerW = chartW - leftPad - rightPad;
  const chartInnerH = chartH - topPad - bottomPad;

  const xFor = (index) => leftPad + (index / (campaign.timeline.length - 1)) * chartInnerW;
  const normalize = (value, min, max) => (max === min ? 0.5 : (value - min) / (max - min));
  const yForNorm = (norm) => topPad + chartInnerH - norm * chartInnerH;

  const pricePath = campaign.timeline
    .map((point, idx) => `${idx === 0 ? "M" : "L"} ${xFor(idx)} ${yForNorm(normalize(point.price, priceMin, priceMax))}`)
    .join(" ");

  const followerPath = campaign.timeline
    .map((point, idx) => `${idx === 0 ? "M" : "L"} ${xFor(idx)} ${yForNorm(normalize(point.followers, followerMin, followerMax))}`)
    .join(" ");

  const kolIndex = campaign.timeline.findIndex((point) => point.date === campaign.postDate);
  const markerX = kolIndex >= 0 ? xFor(kolIndex) : xFor(Math.floor(campaign.timeline.length / 2));

  const xLabels = [0, kolIndex, campaign.timeline.length - 1]
    .filter((value, idx, arr) => value >= 0 && arr.indexOf(value) === idx)
    .map((idx) => {
      const date = campaign.timeline[idx].date;
      return `<g>
        <line x1="${xFor(idx)}" y1="${topPad}" x2="${xFor(idx)}" y2="${chartH - bottomPad}" stroke="rgba(167,180,220,0.20)" />
        <text x="${xFor(idx)}" y="${chartH - 26}" text-anchor="middle" fill="#a7b4dc" font-size="11">${date}</text>
      </g>`;
    })
    .join("");

  mount.innerHTML = `
    <svg viewBox="0 0 ${chartW} ${chartH}" width="100%" height="320" role="img" aria-label="Token price and followers timeline">
      <rect x="${leftPad}" y="${topPad}" width="${chartInnerW}" height="${chartInnerH}" fill="rgba(17,30,71,0.3)" stroke="rgba(167,180,220,0.15)"></rect>
      ${xLabels}
      <line x1="${markerX}" y1="${topPad}" x2="${markerX}" y2="${chartH - bottomPad}" stroke="#ffd76a" stroke-dasharray="5 4"></line>
      <text x="${markerX + 7}" y="${topPad + 14}" fill="#ffd76a" font-size="11">KOL post</text>
      <path d="${pricePath}" fill="none" stroke="#46d6ff" stroke-width="3" />
      <path d="${followerPath}" fill="none" stroke="#7c65ff" stroke-width="3" />
      <text x="${leftPad}" y="${chartH - 8}" fill="#a7b4dc" font-size="12">Price range: ${fmtUsd.format(priceMin)} - ${fmtUsd.format(
        priceMax
      )}</text>
      <text x="${leftPad + 290}" y="${chartH - 8}" fill="#a7b4dc" font-size="12">Followers range: ${fmtInt.format(
        followerMin
      )} - ${fmtInt.format(followerMax)}</text>
      <rect x="${chartW - 220}" y="${chartH - 23}" width="12" height="12" rx="2" fill="#46d6ff"></rect>
      <text x="${chartW - 204}" y="${chartH - 12}" fill="#a7b4dc" font-size="12">Token price (normalized)</text>
      <rect x="${chartW - 220}" y="${chartH - 43}" width="12" height="12" rx="2" fill="#7c65ff"></rect>
      <text x="${chartW - 204}" y="${chartH - 32}" fill="#a7b4dc" font-size="12">Followers (normalized)</text>
    </svg>
  `;
}

function renderPostsTable(containerId, posts) {
  const mount = document.getElementById(containerId);
  if (posts.length === 0) {
    mount.innerHTML = `<p class="muted">No posts available in this period.</p>`;
    return;
  }

  const rows = posts
    .map((post) => {
      const engagement = postEngagement(post);
      return `<tr>
          <td>${post.date}</td>
          <td>${post.text}</td>
          <td>${fmtInt.format(engagement)}</td>
          <td>${toPct((engagement / post.impressions) * 100)}</td>
        </tr>`;
    })
    .join("");

  mount.innerHTML = `
    <table>
      <thead>
        <tr>
          <th>Date</th>
          <th>Post</th>
          <th>Engagement</th>
          <th>Rate</th>
        </tr>
      </thead>
      <tbody>${rows}</tbody>
    </table>
  `;
}

function renderRoiBreakdown(campaign, stats) {
  const mount = document.getElementById("roiBreakdown");
  const net = stats.tokenReturnValue - campaign.costUsd;
  const followerAcquisitionCost = campaign.costUsd / Math.max(stats.followerChange, 1);
  const costPerEngagement = campaign.costUsd / Math.max(sum(stats.afterPosts.map((post) => postEngagement(post))), 1);

  mount.innerHTML = `
    <div class="roi-grid">
      <div class="roi-tile">
        <h4>Campaign Spend</h4>
        <p>${fmtUsd.format(campaign.costUsd)}</p>
      </div>
      <div class="roi-tile">
        <h4>Token Price Return</h4>
        <p class="${asPosNegClass(stats.tokenReturnValue)}">${fmtUsd.format(stats.tokenReturnValue)}</p>
      </div>
      <div class="roi-tile">
        <h4>Net Value</h4>
        <p class="${asPosNegClass(net)}">${fmtUsd.format(net)}</p>
      </div>
      <div class="roi-tile">
        <h4>Cost per New Follower</h4>
        <p>${fmtUsd.format(followerAcquisitionCost)}</p>
      </div>
      <div class="roi-tile">
        <h4>Cost per Engagement</h4>
        <p>${fmtUsd.format(costPerEngagement)}</p>
      </div>
      <div class="roi-tile">
        <h4>KOL Reference</h4>
        <p><a href="${campaign.postUrl}" target="_blank" rel="noopener noreferrer">${campaign.kolHandle}</a></p>
      </div>
    </div>
    <p class="formula">
      ROI formula: ((token return value - campaign spend) / campaign spend) * 100
    </p>
  `;
}

function render(projectId, campaignId) {
  const project = projects.find((item) => item.id === projectId) ?? projects[0];
  const campaign = project.campaigns.find((item) => item.id === campaignId) ?? project.campaigns[0];
  const stats = calculateCampaignStats(campaign);

  renderKpis(project, campaign, stats);
  renderComparisonChart(stats);
  renderTimelineChart(campaign);
  renderPostsTable("topPostsBefore", topPosts(stats.beforePosts));
  renderPostsTable("topPostsAfter", topPosts(stats.afterPosts));
  renderRoiBreakdown(campaign, stats);
}

function seedProjectSelect() {
  projectSelect.innerHTML = projects
    .map((project) => `<option value="${project.id}">${project.name} (${project.tokenSymbol})</option>`)
    .join("");
}

function seedCampaignSelect(projectId) {
  const project = projects.find((item) => item.id === projectId) ?? projects[0];
  campaignSelect.innerHTML = project.campaigns
    .map(
      (campaign) =>
        `<option value="${campaign.id}">${campaign.kolHandle} (${campaign.postDate})</option>`
    )
    .join("");
}

projectSelect.addEventListener("change", (event) => {
  const projectId = event.target.value;
  seedCampaignSelect(projectId);
  render(projectId, campaignSelect.value);
});

campaignSelect.addEventListener("change", (event) => {
  render(projectSelect.value, event.target.value);
});

seedProjectSelect();
seedCampaignSelect(projectSelect.value || projects[0].id);
render(projectSelect.value || projects[0].id, campaignSelect.value);
